public class TiposPrimitivos {
    public static void main(String[] args) {
        // Números Inteiros
        byte numeroByte = 100;
        short numeroShort = 10000;
        int numeroInt = 100000;
        long numeroLong = 10000000000L;

        // Número de Ponto Flutuante
        float numeroFloat = 10.99f;
        double numeroDouble = 10.99d;

        // Caracteres
        char caractere = 'A';

        // Booleanos
        boolean verdadeiro = true;
        boolean falso = false;



        // Exibindo os valores
        System.out.println("byte: " + numeroByte);
        System.out.println("short: " + numeroShort);
        System.out.println("int" + numeroInt);
        System.out.println("long" + numeroLong);
        System.out.println("float" + numeroFloat);
        System.out.println("double" + numeroDouble);
        System.out.println("char" + caractere);
        System.out.println("boolean verdadeiro:" + verdadeiro);
        System.out.println("boolean falson:" + falso);
        


    }
}