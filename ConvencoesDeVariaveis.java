public class ConvencoesDeVariaveis {
    public static void main (String[] args) {



        // Camel Case
        int idadePessoa = 25;
        double salarioAnual = 50000.0;

        // Nomes Significativos
        String nomeCompleto = "João da Silva";
        boolean isEmpregado = true;

        // Constantes
        final double PI = 3.14159;
        final int MAX_TENTATIVAS = 5;

        // Exibindo os valores
        System.out.println("Idade: " + idadePessoa);
        System.out.println("Salário Anual: " + salarioAnual);
        System.out.println("Nome Completo:" + nomeCompleto);
        System.out.println("Empregado:" + isEmpregado);
        System.out.println("PI:" + PI);
        System.out.println("Máximo de Tentativas: " + MAX_TENTATIVAS);
 
    }
}
